void main() {
  // 1. int: Used to represent whole numbers
  int myAge = 30;

  // 2. double: Used to represent floating-point numbers
  double piValue = 3.14;

  // 3. String: Used to represent text values
  String greeting = "Hello, World!";

  // 4. List: Used to store a collection of values
  List<int> numbers = [1, 2, 3, 4, 5];

  // 5. Map: Used to store key-value pairs
  Map<String, dynamic> personInfo = {
    'name': 'Alice',
    'age': 25,
    'isStudent': true,
  };

  // Printing the values
  print("My age: $myAge");
  print("Value of pi: $piValue");
  print("Greeting: $greeting");
  print("Numbers: $numbers");
  print("Person info: $personInfo");
}
